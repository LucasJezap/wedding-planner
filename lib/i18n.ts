export type Messages = (typeof dictionaries)[Locale];

export const DEFAULT_LOCALE: Locale = "pl";

export const resolveLocale = (value?: string | null): Locale =>
  SUPPORTED_LOCALES.includes(value as Locale)
    ? (value as Locale)
    : DEFAULT_LOCALE;

export const getMessages = (locale: Locale): Messages => dictionaries[locale];

export const localeTag = (locale: Locale): string =>
  locale === "pl" ? "pl-PL" : "en-US";
